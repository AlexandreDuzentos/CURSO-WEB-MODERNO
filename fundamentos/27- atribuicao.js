// Atribuição normal
const a = 8
let b = 5

// Atribuição aditiva
b += a // b = b + a
console.log(b)

// Atribuição subtrativa
b -= a // b = b - a
console.log(b)

// Atribuição multiplicativa
b *= a // b = b * a
console.log(b)

// Atribuição divisiva
b /= a // b = b / a
console.log(b)

// Atribuição modular
b %= a // b = b % a
console.log(b)